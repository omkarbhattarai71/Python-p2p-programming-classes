# Take two numbers as input and perform all the arithmetic operations in it
a=int(input("Enter first number::  "))
b=int(input("Enter second number:: "))
print("sum is: {} ".format(a+b))
print("difference is: {} ".format(a-b))
print("Multiplication is: {} ".format(a*b))
print("Float Division is: {}".format(a/b))
print("Integer Division is: {}".format(a//b))
print("Remainder is: {} ".format(a%b))
